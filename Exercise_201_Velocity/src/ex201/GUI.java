    private void onAddMeasure(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_onAddMeasure
        
        VeloDLG vdlg = new VeloDLG(this, true);
        vdlg.setVisible(true);

        if (vdlg.isOk()) {
            vtm.add(vdlg.getData());
        } 
        
        else {
            JOptionPane.showMessageDialog(null, "Adi geht mal wieder nicht :<", "Erawr :Z!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_onAddMeasure

