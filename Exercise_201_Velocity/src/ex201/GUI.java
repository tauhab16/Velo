    private void onSaveData(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_onSaveData
       
        JFileChooser choosi = createFileChooser();
        File f = null;
        int ok = choosi.showSaveDialog(this);
        if (ok == JFileChooser.APPROVE_OPTION) {
            f = choosi.getSelectedFile();

            try {
                vtm.save(f);
            } 
            catch (Exception ex) {
                Logger.getLogger(GUI.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_onSaveData

    private void onLoadData(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_onLoadData
       
        JFileChooser choosi = createFileChooser();

        int ok = choosi.showOpenDialog(this);
        if (ok == JFileChooser.APPROVE_OPTION) {
            File f = choosi.getSelectedFile();

            try {
                vtm.load(f);
            } 
            catch (Exception ex) {
                Logger.getLogger(GUI.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_onLoadData

